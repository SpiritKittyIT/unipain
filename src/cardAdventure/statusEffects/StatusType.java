package cardAdventure.statusEffects;

/**
* enum to represent the type of the StatusEffect.
*/
public enum StatusType {
    /**
    * type of the Poisoned StatusEffect.
    */
    POISONED,
    /**
    * type of the Burning StatusEffect.
    */
    BURNING,
    /**
    * type of the Blessed StatusEffect.
    */
    BLESSED,
    /**
    * type of the Confused StatusEffect.
    */
    CONFUSED,
    /**
    * type of the Inspired StatusEffect.
    */
    INSPIRED
}
